package com.example.pdfgenerator.Pojo;

public class ImsSection {
	private String PSBMember;
	private String PGMName;
	private String DBDName;
	private String DBDProcopt;
	private String Segement;
	private String SegProcopt;
	
	
	public String getPSBMember() {
		return PSBMember;
	}
	public void setPSBMember(String pSBMember) {
		PSBMember = pSBMember;
	}
	public String getPGMName() {
		return PGMName;
	}
	public void setPGMName(String pGMName) {
		PGMName = pGMName;
	}
	public String getDBDName() {
		return DBDName;
	}
	public void setDBDName(String dBDName) {
		DBDName = dBDName;
	}
	public String getDBDProcopt() {
		return DBDProcopt;
	}
	public void setDBDProcopt(String dBDProcopt) {
		DBDProcopt = dBDProcopt;
	}
	public String getSegement() {
		return Segement;
	}
	public void setSegement(String segement) {
		Segement = segement;
	}
	public String getSegProcopt() {
		return SegProcopt;
	}
	public void setSegProcopt(String segProcopt) {
		SegProcopt = segProcopt;
	}
}
