package com.example.pdfgenerator.Pojo;

public class Pl1Details {
	private String jobName;
	private String programName; 
	private String programType;  
	private String subModules ;
	private String copyBooks;
	
	public String getJobName() {
		return jobName;
	}
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}
	public String getProgramName() {
		return programName;
	}
	public void setProgramName(String programName) {
		this.programName = programName;
	}
	public String getProgramType() {
		return programType;
	}
	public void setProgramType(String programType) {
		this.programType = programType;
	}
	public String getSubModules() {
		return subModules;
	}
	public void setSubModules(String subModules) {
		this.subModules = subModules;
	}
	public String getCopyBooks() {
		return copyBooks;
	}
	public void setCopyBooks(String copyBooks) {
		this.copyBooks = copyBooks;
	}
}
